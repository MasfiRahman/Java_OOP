public class Math {
    public static void main(String[] args) {
        System.out.println(java.lang.Math.abs(-9.5));
        System.out.println(java.lang.Math.sqrt(25));
        System.out.println(java.lang.Math.pow(2, 3));
        System.out.println(java.lang.Math.PI);
        System.out.println(java.lang.Math.log(2.0));
        System.out.println(java.lang.Math.exp(1.0));
        System.out.println(java.lang.Math.max(-2.3, -12.8));
        System.out.println(java.lang.Math.min(-2.3, -12.8));
        System.out.println(java.lang.Math.ceil(-8.6));
        System.out.println(java.lang.Math.floor(-8.6));
    }
}
